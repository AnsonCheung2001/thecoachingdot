              <h3>關於教練</h3>
              <p>本人乃熱愛生命的人生教練和家庭教練。善於洞察被教練者的所思所感，以創新的目光，檢視身處的環境，且能適當地發問，讓被教練者將信念化為具體的行動綱領。</p>
              <h5>因視障而走上創作岔路</h5>
              <p>中二，本人確診青光眼；10多年後，便僅餘一成視力。但在朋友鼓勵下，於05年，當時待業的我，推出首部小說《當被遺忘的遇上》，分享面對病患的心路歷程，同年，獲邀成為《am730》專欄作家，於《視角》專欄撰稿至今，以簡潔有力的文字，帶出積極的信息；為了追求寫作夢，並打破視障人士的既定形象，本人撰寫計劃書，並成功獲得香港失明人協進會的「精彩人生‧夢想成真計劃」贊助，於09年，開展了採訪及出版計劃。當中，視障的我採訪了6個不同組織(如：劇團、義工小組等)，以宣揚堅持夢想的理念。</p>
              <p>一年多裡，從聯絡採訪、接觸出版社，以致媒體宣傳，我也親力親為，且能按時完成工作。</p>
              <p>11年，推出散文及訪問集《黑暗也能看見》，並獲多個媒體採訪。當中，《信報》更向本人邀稿，撰寫專欄《另眼看世界》。</p>
              <p>13年初，推出散文集《聽到生命的精采》。而透過突破出版社的安排，於1４至１６年間，先後於10間中、小學(*詳見附表1)，主講寫作講座，同時亦分享跨越視力限制而當上作家之經歷。今天，青年人面對多方面的挑戰，因而感到焦慮和迷惘。本人的故事正正可以鼓勵他們，從認識自身特質與限制開始，學習克服困難，追尋夢想。
              在「追夢」的路上，我體會到：「人人皆有潛能，事事亦有機遇！」懷抱積極的態度和開放的思維，本人渴望透過對話，與被教練者一起發掘自身潛能，確立具體可行的目標，邁向持續成長；</p>
              <h5>從心靈追求卓越</h5>
              <p>本人能突破視障的限制，當上作家，因而當選香港再生會的「十大再生勇士」(06年)及香港復康力量的「在職達人」(11年)，以表揚本人積極「抗逆」的經驗。</p>
              <p>面對視力限制，本人也曾失去追尋理想的動力，甚至減少與親友接觸。</p>
              <p>但感恩，在情緒低落時，家人從不強迫自己參與什麼活動，反而容讓我自由選擇。於是，我修讀了不同的個人成長課程，學習接納自己，嘗試處理內心困擾。慢慢地，我放下了心靈包袱，並重新出發，活躍於創作‘表演和義工等活動(詳見本人網站)。</p>
              <p>過程中，我明白到心靈健康正是喜樂和成功的基石。因此，我立志，以生命影響生命，讓更多人心靈得到釋放，繼而發揮自己，影響他人。</p>
              <p>同時，我也體會到，在個人成長上，家人的接納和支持也是舉足輕重的。因此，本人渴望從事家長相關的工作，讓家長能看清自己的角色，發現與子女溝通之技巧，繼而找出一起成長的有效方法。</p>
              <p>10年，本人開始教授溝通技巧、情緒管理等課程。透過觀賞、感恩、信念改寫等簡單練習，參加者可以輕鬆地放下負面情緒，並掌握處理衝突的技巧，繼而與人建立良好關係。</p>
              <p>作為導師，本人善於深入聆聽，且能作出即場互動。因此，多數參加者均感到被受尊重，繼而敢於開啟心靈，進行深層次分享。而敢於分享，正是生命突破的第一步！</p>
              <p>看到參加者有所學習和成長，本人實在感到滿足和喜樂，這樣，我便更加確信自己的使命：讓人獲得心靈健康。</p>
              <p>因著這個使命，我開始修讀教練學。15至16年，本人修讀了「國際教練協會（ International  Association of Coaching）」所編製的 「IAC 教練學九大精粹課程，課堂時數50小時，導師個別指導10小時及實習40小時。當中，透過電話和coaching café的面談，本人就曾與各行業的在職人士、學生、以及其他專業教練，進行教練啟導。多數被教練者均指出，本人能細心而主動地聆聽，發現他們的限制所在，且能恰當地提問，誘導他們發現自身潛能，訂立確切具體的計劃。這正正乎合本人的Strength Finder測試結果──本人其中的優勢包括：思維、聯想和成就。因此，在教練對話中，本人樂意專注當下，細心分析被教練者的處境，並誘導對方尋找具體可行的方法。</p>
              <p>今天，本人定意將教練心態融入生活，好讓自己成為卓越的教練。抱著積極的心態，開放而耐心地聆聽每一位被教練者，讓他們能深入發現自己，並親手寫出自己的動人故事。</p>
